package bean;

import java.io.Serializable;
import java.util.Date;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.ManyToOne;
import javax.persistence.Temporal;

/**
 *
 * @author khalid
 */
@Entity
public class TaxeContruction implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;
    @Temporal(javax.persistence.TemporalType.DATE)
    private Date datePresentation;
    private Double montant;
    private Double metrage;
    @ManyToOne
    private TauxTaxe tauxTaxe;
    @ManyToOne
    private Redevable redevable;
    @ManyToOne
    private LocaleConstruction localeConstruction;

    public Date getDatePresentation() {
        return datePresentation;
    }

    public void setDatePresentation(Date datePresentation) {
        this.datePresentation = datePresentation;
    }

    public Double getMontant() {
        return montant;
    }

    public void setMontant(Double montant) {
        this.montant = montant;
    }

    public Double getMetrage() {
        return metrage;
    }

    public void setMetrage(Double metrage) {
        this.metrage = metrage;
    }

    public TauxTaxe getTauxTaxe() {
        return tauxTaxe;
    }

    public void setTauxTaxe(TauxTaxe tauxTaxe) {
        this.tauxTaxe = tauxTaxe;
    }

    public Redevable getRedevable() {
        return redevable;
    }

    public void setRedevable(Redevable redevable) {
        this.redevable = redevable;
    }

  
    public TaxeContruction() {
    }

   

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof TaxeContruction)) {
            return false;
        }
        TaxeContruction other = (TaxeContruction) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Taxe{" + "id=" + id + ", datePresentation=" + datePresentation + ", montant=" + montant + ", metrage=" + metrage + '}';
    }

}
